# Lista de colores  
colores = ["rojo", "azul", "verde", "amarillo", "marrón", "lila", "negro", "rosa"]  

print(colores[3])  # Imprimir color en la posición 3 (amarillo)  

# Posición del color 'rojo' y 'rosa'  
print(colores.index("rojo"))  # Índice de "rojo" (0)  
print(colores.index("rosa"))  # Índice de "rosa" (7)  

