x = 0  
while x <= 15:  # Ejecutar mientras x sea menor o igual a 15
    print(x)  # Imprimir el valor de x
    x += 5  # Incrementar x en 5 en cada iteración

x = 0  
while x >= -100:  # Ejecutar mientras x sea mayor o igual a -100
    print(x)  # Imprimir el valor de x
    x -= 20  # Decrementar x en 20 en cada iteración

x = 10  
while x >= 0:  # Ejecutar mientras x sea mayor o igual a 0
    print("El valor del bucle es:", x)  # Imprimir el valor de x
